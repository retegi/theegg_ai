#Tarea 94, el archivo calculadora.py es un módulo que contiene 4 funciones de cálculo.
#Estas funciones pueden ser utilizadas de la siguiente manera: calculadora.suma(1,2)

def suma(n1,n2):
    return(n1+n2)

def resta(n1,n2):
    return(n1-n2)

def producto(n1,n2):
    return(n1*n2)

def division(n1,n2):
    return(n1/n2)